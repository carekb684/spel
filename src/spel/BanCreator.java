    public static void loadBana(ArrayList<Tile> banaList) {
        int y = 0;
        for (String str : bana1[level]) {
            int x = 0;
            for (char ch : str.toCharArray()) {

                switch (ch) {
                    case 'R':
                        banaList.add(new RedTile(40 * x, 40 * y, 38));
                        break;
                    case 'P':
                        banaList.add(new PinkTile(40 * x, 40 * y, 38));
                        break;
                    case 'V':
                        banaList.add(new VioletTile(40 * x, 40 * y, 38));
                        break;
                    case '-':
                        banaList.add(new BlackTile(40 * x, 40 * y, 38, true,false));
                        break;
                    case 'B':
                        banaList.add(new BlueTile(40 * x, 40 * y, 38));
                        break;
                    case 'Y':
                        banaList.add(new YellowTile(40 * x, 40 * y, 38));
                        break;
                    case 'G':
                        banaList.add(new GreenTile(40 * x, 40 * y, 38));
                        break;
                    case '#':
                        banaList.add(new BlackTile(40 * x, 40 * y, 38, false,false));
                        break;
                }
                
//                if(ch == '+' && VioletTile.addBlackTile){
//                   banaList.add(new BlackTile(40 * x, 40 * y, 38, false,true)); 
//                }
                
                
                x++;
            }
            y++;
        }
    }
